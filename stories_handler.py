story_sessions = {}  # {user_id: {"story_id": int, "q_index": int, "score": int}}


def get_stories_menu():
    lines = ["📖 *English Stories — Pilih Cerita:*\n"]
    for s in STORIES:
        lines.append(f"*{s['id']}.* {s['emoji']} _{s['title']}_")
        lines.append(f"   Level: {s['level']} · +{s['xp_complete']} XP\n")
    lines.append("Ketik angka *1–5* untuk mulai membaca!")
    return "\n".join(lines)


def is_choosing_story(user_id):
    s = story_sessions.get(user_id)
    return s and s.get("q_index") == -2


def is_in_story(user_id):
    s = story_sessions.get(user_id)
    return s is not None and s.get("story_id") is not None


def start_story_menu(user_id):
    story_sessions[user_id] = {"story_id": None, "q_index": -2, "score": 0}
    return get_stories_menu()


def handle_story_choice(user_id, text):
    try:
        num = int(text.strip())
        story = next((s for s in STORIES if s["id"] == num), None)
        if story:
            story_sessions[user_id] = {
                "story_id": story["id"],
                "q_index": -1,
                "score": 0,
            }
            return _format_story_content(story), True
    except ValueError:
        pass
    return f"Pilih angka *1* sampai *{len(STORIES)}* ya!", False


def handle_story_input(user_id, text):
    """Returns (message, xp_gained)."""
    session = story_sessions.get(user_id)
    if not session or session.get("story_id") is None:
        return None, 0

    story = next((s for s in STORIES if s["id"] == session["story_id"]), None)
    if not story:
        return None, 0

    q_idx = session["q_index"]

    # -1 = still reading, waiting for "lanjut"
    if q_idx == -1:
        if "lanjut" in text.lower() or text.strip().lower() in ("next", "mulai", "ok"):
            session["q_index"] = 0
            return _format_question(story, 0), 0
        return "Ketik *lanjut* kalau sudah selesai baca ya! 😊", 0

    # Answering MCQ
    answer = text.strip().upper()
    if answer not in ["A", "B", "C", "D"]:
        return "Jawab dengan huruf *A*, *B*, *C*, atau *D* ya!", 0

    q = story["questions"][q_idx]
    is_correct = answer == q["answer"]
    xp_gained = 0

    if is_correct:
        session["score"] += 1
        feedback = f"✅ *Benar!*\n_{q['explanation']}_"
        xp_gained = 20
    else:
        correct_val = q["options"][q["answer"]]
        feedback = (
            f"❌ *Kurang tepat.*\n"
            f"Jawaban benar: *{q['answer']}. {correct_val}*\n"
            f"_{q['explanation']}_"
        )

    session["q_index"] += 1
    next_idx = session["q_index"]

    if next_idx < len(story["questions"]):
        next_q = _format_question(story, next_idx)
        return feedback + "\n\n" + next_q, xp_gained

    # Story complete
    score = session["score"]
    total = len(story["questions"])
    del story_sessions[user_id]
    bonus_xp = story["xp_complete"] if score == total else story["xp_complete"] // 2
    total_xp = xp_gained + bonus_xp

    stars = "⭐" * score + "☆" * (total - score)
    praise = "Sempurna! Pemahaman kamu luar biasa! 🌟" if score == total else "Bagus! Terus semangat belajar ya! 💪"

    result = (
        f"{feedback}\n\n"
        f"━━━━━━━━━━━━━━━━━━\n"
        f"🎉 *Cerita Selesai!* {stars}\n\n"
        f"Skor: *{score}/{total}*\n"
        f"XP diperoleh: *+{total_xp} XP*\n\n"
        f"{praise}\n\n"
        f"Ketik /story untuk baca cerita lain!"
    )
    return result, total_xp


def cancel_story(user_id):
    if user_id in story_sessions:
        del story_sessions[user_id]


def _format_story_content(story):
    return (
        f"📖 *{story['emoji']} {story['title']}*\n"
        f"_{story['level']} · +{story['xp_complete']} XP_\n\n"
        f"━━━━━━━━━━━━━━━━━━\n\n"
        f"{story['content']}\n\n"
        f"━━━━━━━━━━━━━━━━━━\n\n"
        f"Sudah selesai baca? Ketik *lanjut* untuk mulai soal pemahaman!"
    )


def _format_question(story, q_idx):
    q = story["questions"][q_idx]
    total = len(story["questions"])
    lines = [
        f"❓ *Pertanyaan {q_idx + 1}/{total}*\n",
        q["q"] + "\n",
    ]
    for key, val in q["options"].items():
        lines.append(f"  *{key}.* {val}")
    lines.append("\n_Ketik A, B, C, atau D_")
    return "\n".join(lines)

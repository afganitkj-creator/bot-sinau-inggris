import random

def get_category_list():
    lines = ["Pilih topik kosakata:\n"]
    for i, (key, val) in enumerate(VOCAB_CATEGORIES.items(), 1):
        lines.append(f"{i}. {val['label']}")
    lines.append(f"{len(VOCAB_CATEGORIES)+1}. 🎲 Acak (Random semua topik)")
    return "\n".join(lines)

def get_words_for_category(category_key):
    if category_key == "random":
        all_words = []
        for cat in VOCAB_CATEGORIES.values():
            all_words.extend(cat["words"])
        return random.sample(all_words, min(10, len(all_words)))
    cat = VOCAB_CATEGORIES.get(category_key)
    if cat:
        words = cat["words"].copy()
        random.shuffle(words)
        return words[:10]
    return []

def get_category_by_number(num):
    keys = list(VOCAB_CATEGORIES.keys())
    if num == len(keys) + 1:
        return "random"
    if 1 <= num <= len(keys):
        return keys[num - 1]
    return None

